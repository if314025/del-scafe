    private void getRole(int idRole) throws SQLException
    {
        String query = "SELECT deskripsi FROM role WHERE id_role="
                + idRole;
        PreparedStatement statement = connection.prepareStatement(query);
        ResultSet resultDeskrpsi = statement.executeQuery();
        while(resultDeskrpsi.next())
        {
            
            setGroup(resultDeskrpsi.getString("deskripsi"));
        }
        
        statement.close();
        resultDeskrpsi.close();
    }
