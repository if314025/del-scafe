    private void doQuery() throws SQLException
    {
        String query = "SELECT * FROM account WHERE Username=? AND Password=?";
        preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, jTextField1.getText());
        preparedStatement.setString(2, jPasswordField1.getText());
        resultSet = preparedStatement.executeQuery();
        clearField();
    }
    
    private void openFrame(String role) throws Exception
    {
        if(role.equalsIgnoreCase("admin"))
        {
            IndexAdmin index = new IndexAdmin();
            index.setVisible(true);
            index.runJGroup();
        }
        else if(role.equalsIgnoreCase("member"))
        {
            IndexMember index = new IndexMember();
            index.setVisible(true);
            index.runJGroup();
        }
        
        this.dispose();
        
    }


