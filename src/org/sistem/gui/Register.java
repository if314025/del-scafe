    public void runJGroup() throws Exception
    {
        channel = new JChannel();
        channel.connect(myGroup);
        doReceive();
    }
    
    public void doReceive()
    {
        channel.setReceiver(new ReceiverAdapter()
        {
            public void receive(Message msg)
            {
                
                jTextArea1.setText(jTextArea1.getText().trim()
                        + "\n"
                        + msg.getObject()
                        + "");
            }
        });
    }
    
    public void sendMessage() throws Exception
    {
        channel.send(new Message(null, "user dengan data :"
                + "\nNama\t: " + nama
                + "\nNik\t: " + nik
                + "\nEmail\t: " + email
                + "\nUsername\t: " + username
                + "\nPassword\t: " + password
                + "\nrole\t: " + role
                + "\nBerhasil ditambahkan\n"));
        defaultValue();
    }
    private void tambahDataUser(String nama, String nik ,String email, String username, String password, String role){
        try
        {
            String query = "INSERT INTO account (Nama, Nik, Email, Username, Password, role) VALUES (?,?,?,?,?,?)";
            preparedStatement = myConnection.prepareStatement(query);
            preparedStatement.setString(1, nama);
            preparedStatement.setString(2, nik);
            preparedStatement.setString(3, email);
            preparedStatement.setString(4, username);
            preparedStatement.setString(5, password);
            preparedStatement.setString(6, role);
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Data User berhasil ditambahkan.");
            
            preparedStatement.close();
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "ERROR SQL Inventori");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "ERROR");
        }
    }
    
