     private boolean validation(String nama, String nik, String email,String username,String password, String role)
    {
        if(nama.equalsIgnoreCase(""))
        {
            return false;
        }
        else if(nik.equalsIgnoreCase(""))
        {
            return false;
        }
        else if(email.equalsIgnoreCase(""))
        {
            return false;
        }
        else if(username.equalsIgnoreCase(""))
        {
            return false;
        }
        else if(password.equalsIgnoreCase(""))
        {
            return false;
        }
        else if(role.equalsIgnoreCase(""))
        {
            return false;
        }
        
        return true;
    }
     
